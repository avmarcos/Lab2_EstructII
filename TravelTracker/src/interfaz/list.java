    private void todosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_todosActionPerformed
        String source = source_field.getText();
        
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        
        this.routes = new ArrayList<>();
        
        for (Airport destination: this.airports.airports) {            
            ArrayList<Airport> list = this.airports.travel(source, destination.code);
            
            if (list.isEmpty())
                continue;

            Airport prev = null;
            
            model.addRow(new Object[]{"Camino a",destination.name, "-", "-", destination.lat, destination.lon, "-"});
            
            for (Airport airport: list) {
                float distance = 0;
                if (prev != null)
                    distance = prev.distance(airport);


                model.addRow(new Object[]{airport.code, airport.name, airport.city, airport.county, airport.lat, airport.lon, distance + " km"});

                prev = airport;
            }
            
            routes.add(list);
        }
    }//GEN-LAST:event_todosActionPerformed


