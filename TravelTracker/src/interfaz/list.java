    private void lejanosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lejanosActionPerformed
        String source = source_field.getText();
        
        ArrayList<Airport> list = this.airports.furthestAirports(source);
        
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        
        
        Airport source_airport = this.airports.searchAirport(source);
        
        for (Airport airport: list) {
            float distance = this.airports.airportDistance(source_airport, airport);
            
            model.addRow(new Object[]{airport.code, airport.name, airport.city, airport.county, airport.lat, airport.lon, distance + " km"});
        }
        
        this.routes = new ArrayList<>();
        for (Airport destination: list) {
            ArrayList<Airport> route = this.airports.travel(source_airport.code, destination.code);
            routes.add(route);
        }
        
    }//GEN-LAST:event_lejanosActionPerformed

    public void showAll () {
        
        this.routes = new ArrayList<>();
        for (int i = 0; i < this.airports.airports.size(); i++) {
            ArrayList<Airport> route = new ArrayList<Airport>();
            route.add(this.airports.airports.get(i));
            routes.add(route);
        }
        
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        
        for (Airport airport: airports.airports) {
            model.addRow(new Object[]{airport.code, airport.name, airport.city, airport.county, airport.lat, airport.lon, 0});
        }
    }
    
    
    private void caminoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_caminoActionPerformed
        String source = source_field.getText();
        String destination = destination_field.getText();
        
        ArrayList<Airport> list = this.airports.travel(source, destination);
        
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        
        Airport prev = null;
        
        for (Airport airport: list) {
            float distance = 0;
            if (prev != null)
                distance = prev.distance(airport);
            
            
            model.addRow(new Object[]{airport.code, airport.name, airport.city, airport.county, airport.lat, airport.lon, distance + " km"});

            prev = airport;
        }
        
        this.routes = new ArrayList<>();
        routes.add(list);
        
    }//GEN-LAST:event_caminoActionPerformed

